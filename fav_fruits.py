favourite_fruits = ['Graphes','Litchi','Apple','Orange','Pearl']

fruit = ['Apple','Banana','Litchi','Orange','Chiku']

for index in range(len(fruit)):
    if fruit[index] in favourite_fruits:
        print(f'I really like {fruit[index]}!')
    
    else:
        print(f"\nI don't like {fruit[index]}!")




